        </div>
        <div class='terms_detail_list'>
            <div class='list-wrapper'>2.</div>
            <p class='list-text'>회사는 천재지변 또는 이에 준하는 불가항력, 정보통신설비의 보수 점검, 교체 또는 고장, 통신의 두절 등으로 인하여 일시적 또는 종국적으로 서비스를 제공할 수 없는 경우, 서비스 제공에 관한 책임이 면제됩니다.</p>
        </div>
