        </div>
        <div class='terms_detail_list'>
            <div class='list-wrapper'>4.</div>
            <p class='list-text'>회사는 회사가 제공하는 거래서비스를 통하지 아니하고 이루어지는 개별계약과 회원본인이 제공한 신상등록정보의 오류, 미비 등으로 인하여 회원이 입는 손해에 대해서는 책임을 지지 않습니다.</p>
        </div>
        <div class='terms_detail_list'>
